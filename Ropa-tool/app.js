function renameRecord(recordId) {
  const roleState = getRoleState();
  const record = roleState.records.find((item) => item.id === recordId);
  if (!record) {
    return;
  }

  const nextName = window.prompt(t("renameRecordPrompt"), record.productName || "");
  if (nextName === null) {
    return;
  }

  roleState.currentRecordId = record.id;
  record.productName = nextName.trim();
  persistState("statusRecordRenamed", {}, { touchRecord: true });
  render();
}

function deleteRecord(recordId) {
  const roleState = getRoleState();
  const record = roleState.records.find((item) => item.id === recordId);

  if (!record) {
    return;
  }

  if (!window.confirm(state.language === "zh"
    ? "确认删除该产品或服务记录？删除后，其下所有数据处理活动也会一并删除。"
    : "Delete this product or service record? All processing activities under it will also be deleted.")) {
    return;
  }

  roleState.records = roleState.records.filter((item) => item.id !== recordId);
  roleState.deletedRecords = [
    {
      ...deepClone(record),
      deletedAt: new Date().toISOString(),
    },
    ...(roleState.deletedRecords || []),
  ];

  if (roleState.currentRecordId === recordId) {
    roleState.currentRecordId = roleState.records[0]?.id || "";
  }

  persistState("statusRecordDeleted", {}, { touchRecord: false });
  render();
}

