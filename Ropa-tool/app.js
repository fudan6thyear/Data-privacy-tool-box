function loadState() {
  try {
    const stored = localStorage.getItem(storageKey) || localStorage.getItem(legacyStorageKey);
    if (!stored) {
      return createDefaultState();
    }

    const parsed = JSON.parse(stored);
    if (!localStorage.getItem(storageKey)) {
      localStorage.setItem(storageKey, stored);
    }
    return {
      ...createDefaultState(),
      ...parsed,
      language: parsed.language === "zh" ? "zh" : "en",
      previewScope: parsed.previewScope === "global" ? "global" : "current",
      controller: hydrateRoleState("controller", parsed.controller),
      processor: hydrateRoleState("processor", parsed.processor),
    };
  } catch (error) {
    console.error("Failed to load saved state", error);
    return createDefaultState();
  }
}

function hydrateRoleState(role, value) {
  const base = createEmptyRoleState(role);
  if (!value) {
    return base;
  }

  const migratedRecords =
    value.records && value.records.length
      ? value.records
      : [
          {
            id: createId("record"),
            productName: "",
            activities: (value.activities || []).map((activity) => ({
              ...normalizeActivity(role, activity),
            })),
            createdAt: value.updatedAt || new Date().toISOString(),
            updatedAt: value.updatedAt || new Date().toISOString(),
          },
        ];

  const hydratedRecords = migratedRecords.map((record) => ({
    ...record,
    id: record.id || createId("record"),
    productName: record.productName || "",
    updatedAt: record.updatedAt || value.updatedAt || new Date().toISOString(),
    createdAt: record.createdAt || record.updatedAt || new Date().toISOString(),
    activities:
      record.activities && record.activities.length
        ? record.activities.map((activity) => ({
            ...normalizeActivity(role, activity),
          }))
        : [],
  }));

  const hydratedDeletedRecords = Array.isArray(value.deletedRecords)
    ? value.deletedRecords.map((record) => ({
        ...record,
        id: record.id || createId("record"),
        productName: record.productName || "",
        updatedAt: record.updatedAt || value.updatedAt || new Date().toISOString(),
        createdAt: record.createdAt || record.updatedAt || new Date().toISOString(),
        deletedAt: record.deletedAt || record.updatedAt || new Date().toISOString(),
        activities:
          record.activities && record.activities.length
            ? record.activities.map((activity) => ({
                ...normalizeActivity(role, activity),
              }))
            : [],
      }))
    : [];

  const currentRecordId =
    hydratedRecords.find((record) => record.id === value.currentRecordId)?.id || hydratedRecords[0]?.id || "";

  return {
    ...base,
    ...value,
    currentRecordId,
    records: hydratedRecords,
    deletedRecords: hydratedDeletedRecords,
    contacts: {
      ...base.contacts,
      ...(value.contacts || {}),
      organisation: { ...base.contacts.organisation, ...(value.contacts?.organisation || {}) },
      dpo: { ...base.contacts.dpo, ...(value.contacts?.dpo || {}) },
      representative: { ...base.contacts.representative, ...(value.contacts?.representative || {}) },
    },
  };
}

function ensureRoleState(role = state.activeRole) {
  const roleState = state[role];
  if (!Array.isArray(roleState.records)) {
    roleState.records = [];
  }
  if (!Array.isArray(roleState.deletedRecords)) {
    roleState.deletedRecords = [];
  }
  if (!roleState.records.some((record) => record.id === roleState.currentRecordId)) {
    roleState.currentRecordId = roleState.records[0]?.id || "";
  }
  return roleState;
}

function getRoleState(role = state.activeRole) {
  return ensureRoleState(role);
}

function getCurrentRecord(role = state.activeRole) {
  const roleState = getRoleState(role);
  return roleState.records.find((record) => record.id === roleState.currentRecordId) || roleState.records[0];
}

function getPreviewRows() {
  const roleState = getRoleState();
  if (state.previewScope === "global") {
    return roleState.records.flatMap((record) =>
      record.activities.map((activity) => ({ record, activity })),
    );
  }

  const currentRecord = getCurrentRecord();
  return currentRecord ? currentRecord.activities.map((activity) => ({ record: currentRecord, activity })) : [];
}

function persistState(statusKey, params = {}, options = {}) {
  const roleState = getRoleState();
  const now = new Date().toISOString();

  if (options.touchRecord) {
    const currentRecord = getCurrentRecord();
    if (currentRecord) {
      currentRecord.updatedAt = now;
      const recordIndex = roleState.records.findIndex((record) => record.id === currentRecord.id);
      if (recordIndex > 0) {
        roleState.records.splice(recordIndex, 1);
        roleState.records.unshift(currentRecord);
        roleState.currentRecordId = currentRecord.id;
      }
    }
  }

  roleState.updatedAt = now;
  saveState();
  setStatus(statusKey, params);
}

function saveState() {
  localStorage.setItem(storageKey, JSON.stringify(state));
}

