function renderHistory() {
  const roleState = getRoleState();
  dom.historyList.innerHTML = "";

  if (!roleState.records.length) {
    const emptyState = document.createElement("div");
    emptyState.className = "empty-state";
    emptyState.textContent = t("historyEmpty");
    dom.historyList.appendChild(emptyState);
    return;
  }

  roleState.records.forEach((record, index) => {
    const item = document.createElement("div");
    item.className = `history-item${record.id === roleState.currentRecordId ? " active" : ""}`;

    const button = document.createElement("button");
    button.type = "button";
    button.className = "history-item__main";
    button.innerHTML = `
      <strong>${getRecordTitle(record, index)}</strong>
      <span>${t("historyCount", { count: record.activities.length })}</span>
      <span>${formatSavedAt(record.updatedAt)}</span>
    `;
    button.addEventListener("click", () => {
      roleState.currentRecordId = record.id;
      saveState();
      setStatus("statusRecordSelected");
      renderSummary();
      renderCurrentRecordControls();
      renderHistory();
      renderTrash();
      renderActivities();
      renderPreview();
    });

    const actions = document.createElement("div");
    actions.className = "history-item__actions";

    const renameButton = document.createElement("button");
    renameButton.type = "button";
    renameButton.className = "button button--secondary button--small";
    renameButton.textContent = t("rename");
    renameButton.addEventListener("click", () => {
      renameRecord(record.id);
    });

    const deleteButton = document.createElement("button");
    deleteButton.type = "button";
    deleteButton.className = "button button--ghost button--small";
    deleteButton.textContent = t("remove");
    deleteButton.addEventListener("click", () => {
      deleteRecord(record.id);
    });

    actions.append(renameButton, deleteButton);
    item.append(button, actions);
    dom.historyList.appendChild(item);
  });
}

function renderTrash() {
  const roleState = getRoleState();
  dom.trashList.innerHTML = "";
  dom.trashCount.textContent = String(roleState.deletedRecords.length);

  if (!roleState.deletedRecords.length) {
    const emptyState = document.createElement("div");
    emptyState.className = "empty-state";
    emptyState.textContent = t("trashEmpty");
    dom.trashList.appendChild(emptyState);
    return;
  }

  roleState.deletedRecords.forEach((record, index) => {
    const item = document.createElement("div");
    item.className = "history-item";

    const main = document.createElement("div");
    main.className = "history-item__main history-item__main--static";
    main.innerHTML = `
      <strong>${getRecordTitle(record, index)}</strong>
      <span>${t("historyCount", { count: record.activities.length })}</span>
      <span>${formatSavedAt(record.deletedAt || record.updatedAt)}</span>
    `;

    const actions = document.createElement("div");
    actions.className = "history-item__actions";

    const restoreButton = document.createElement("button");
    restoreButton.type = "button";
    restoreButton.className = "button button--secondary button--small";
    restoreButton.textContent = t("restore");
    restoreButton.addEventListener("click", () => {
      const restored = deepClone(record);
      delete restored.deletedAt;
      roleState.deletedRecords = roleState.deletedRecords.filter((item) => item.id !== record.id);
      roleState.records.unshift(restored);
      roleState.currentRecordId = restored.id;
      persistState("statusRecordRestored", {}, { touchRecord: false });
      render();
    });

    actions.appendChild(restoreButton);
    item.append(main, actions);
    dom.trashList.appendChild(item);
  });
}

function renderActivities() {
  const config = roleConfigs[state.activeRole];
  const currentRecord = getCurrentRecord();
  const editableFields = getEditableFields(config);
  dom.activityList.innerHTML = "";

  if (!currentRecord || !currentRecord.activities.length) {
    const emptyState = document.createElement("div");
    emptyState.className = "empty-state";
    emptyState.textContent = t("emptyActivities");
    dom.activityList.appendChild(emptyState);
    return;
  }

  currentRecord.activities.forEach((activity, index) => {
    const card = document.createElement("details");
    card.className = `activity-card activity-card--tone-${index % 4}${activity.locked ? " is-locked" : ""}`;
    card.open = index === 0;

    const summary = document.createElement("summary");
    const activeFields = editableFields.filter((field) => isFieldVisible(activity, field));
    const requiredCount = editableFields.filter((field) => field.required).length;
    const completionCount = activeFields.filter((field) => isFieldCompleted(activity, field)).length;

    const titleWrap = document.createElement("div");
    titleWrap.innerHTML = `
      <h3>${getActivityTitle(activity, state.activeRole, index)}</h3>
      <div class="activity-card__meta">
        <span class="pill">${t("activityLabel", { index: index + 1 })}</span>
        <span class="pill">${t("fieldsCompleted", { completed: completionCount, total: activeFields.length })}</span>
        <span class="pill">${t("coreFields", { count: requiredCount })}</span>
        <span class="pill">${activity.locked ? t("locked") : t("unlocked")}</span>
      </div>
    `;

    const actions = document.createElement("div");
    actions.className = "activity-card__actions";

    const lockButton = document.createElement("button");
    lockButton.type = "button";
    lockButton.className = "button button--secondary";
    lockButton.textContent = activity.locked ? t("unlock") : t("lock");
    lockButton.addEventListener("click", (event) => {
      event.preventDefault();
      event.stopPropagation();
      activity.locked = !activity.locked;
      activity.updatedAt = new Date().toISOString();
      persistState(activity.locked ? "statusActivityLocked" : "statusActivityUnlocked", {}, { touchRecord: true });
      render();
    });

    const duplicateButton = document.createElement("button");
    duplicateButton.type = "button";
    duplicateButton.className = "button button--ghost";
    duplicateButton.textContent = t("duplicate");
    duplicateButton.disabled = activity.locked;
    duplicateButton.addEventListener("click", (event) => {
      event.preventDefault();
      event.stopPropagation();
      const clonedActivity = {
        ...deepClone(activity),
        id: createId("activity"),
        locked: false,
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      };
      currentRecord.activities.unshift(clonedActivity);
      persistState("statusActivityDuplicated", {}, { touchRecord: true });
      render();
    });

    const removeButton = document.createElement("button");
    removeButton.type = "button";
    removeButton.className = "button button--ghost";
    removeButton.textContent = t("remove");
    removeButton.disabled = activity.locked;
    removeButton.addEventListener("click", (event) => {
      event.preventDefault();
      event.stopPropagation();
      currentRecord.activities = currentRecord.activities.filter((item) => item.id !== activity.id);
      persistState("statusActivityRemoved", {}, { touchRecord: true });
      render();
    });

    actions.append(lockButton, duplicateButton, removeButton);
    summary.append(titleWrap, actions);

    const body = document.createElement("div");
    body.className = "activity-card__body";

    config.sections.forEach((section) => {
      const subsection = document.createElement("section");
      subsection.className = "subsection";

      const sectionHeader = document.createElement("div");
      sectionHeader.className = "subsection__header";
      sectionHeader.innerHTML = `
        <div>
          <h4>${tx(section.title)}</h4>
          <p>${tx(section.blurb)}</p>
        </div>
      `;

      const grid = document.createElement("div");
      grid.className = "field-grid";

      editableFields
        .filter((field) => field.section === section.key)
        .filter((field) => isFieldVisible(activity, field))
        .forEach((field) => {
          if (field.type === "multiCheckbox") {
            grid.appendChild(createMultiCheckboxField(activity, field));
            return;
          }

          if (field.type === "recipientsTable") {
            grid.appendChild(createRecipientsTableField(activity, field));
            return;
          }

          if (field.type === "transferTable") {
            grid.appendChild(createTransferTableField(activity, field));
            return;
          }

          grid.appendChild(
            createField({
              field,
              value: activity[field.key] || "",
              disabled: activity.locked,
              onInput: (value) => {
                activity[field.key] = value;
                if (field.key === "automatedDecisionMaking" && value !== "Yes") {
                  activity.automatedDecisionImpact = "No";
                }
                activity.updatedAt = new Date().toISOString();
                persistState("statusActivitySaved", {}, { touchRecord: true });
                renderSummary();
                renderCurrentRecordControls();
                renderHistory();
                renderTrash();
                if (["automatedDecisionMaking", "automatedDecisionImpact"].includes(field.key)) {
                  renderActivities();
                }
                renderPreview();
              },
            }),
          );
        });

      subsection.append(sectionHeader, grid);

      if (section.key === "lawful" && state.activeRole === "controller") {
        subsection.appendChild(renderRightsSummary(activity));
      }

      body.appendChild(subsection);
    });

    card.append(summary, body);
    dom.activityList.appendChild(card);
  });
}

