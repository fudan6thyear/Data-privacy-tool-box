function createTableField({ activity, field, rows, columns, createRow, onChange }) {
  const wrapper = document.createElement("div");
  wrapper.className = "field field--wide table-field";

  const label = createFieldLabel(field);
  const tableWrap = document.createElement("div");
  tableWrap.className = "table-editor";

  const table = document.createElement("table");
  table.className = "table-editor__table";

  const thead = document.createElement("thead");
  const headRow = document.createElement("tr");
  columns.forEach((column) => {
    const th = document.createElement("th");
    th.textContent = column.label;
    headRow.appendChild(th);
  });
  const actionTh = document.createElement("th");
  actionTh.textContent = "";
  headRow.appendChild(actionTh);
  thead.appendChild(headRow);

  const tbody = document.createElement("tbody");
  const safeRows = rows.length ? rows : [createRow()];

  safeRows.forEach((row, rowIndex) => {
    const tr = document.createElement("tr");

    columns.forEach((column) => {
      const td = document.createElement("td");
      if (column.readOnly) {
        td.textContent = String(rowIndex + 1);
      } else {
        const input = document.createElement("input");
        input.type = column.type || "text";
        input.value = row[column.key] || "";
        input.disabled = activity.locked;
        input.addEventListener("input", (event) => {
          const updatedRows = safeRows.map((item) => ({ ...item }));
          updatedRows[rowIndex][column.key] = event.target.value;
          onChange(updatedRows, { rerenderActivities: false });
        });
        td.appendChild(input);
      }
      tr.appendChild(td);
    });

    const actionTd = document.createElement("td");
    const removeButton = document.createElement("button");
    removeButton.type = "button";
    removeButton.className = "button button--ghost button--small";
    removeButton.textContent = t("removeRow");
    removeButton.disabled = activity.locked || safeRows.length === 1;
    removeButton.addEventListener("click", () => {
      const updatedRows = safeRows.filter((_, index) => index !== rowIndex);
      onChange(updatedRows.length ? updatedRows : [createRow()], { rerenderActivities: true });
    });
    actionTd.appendChild(removeButton);
    tr.appendChild(actionTd);
    tbody.appendChild(tr);
  });

  table.append(thead, tbody);
  tableWrap.appendChild(table);

  const footer = document.createElement("div");
  footer.className = "table-editor__footer";
  const addButton = document.createElement("button");
  addButton.type = "button";
  addButton.className = "button button--secondary button--small";
  addButton.textContent = t("addRow");
  addButton.disabled = activity.locked;
  addButton.addEventListener("click", () => {
    onChange([...safeRows.map((item) => ({ ...item })), createRow()], { rerenderActivities: true });
  });
  footer.appendChild(addButton);

  const help = document.createElement("p");
  help.className = "field-help";
  help.textContent = field.required ? t("fieldHelpRequired") : t("fieldHelpOptional");

  wrapper.append(label, tableWrap, footer, help);
  return wrapper;
}

