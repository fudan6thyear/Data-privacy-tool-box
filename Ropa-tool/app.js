function isFieldCompleted(activity, field) {
  if (field.type === "multiCheckbox") {
    return Array.isArray(activity[field.key]) && activity[field.key].length > 0;
  }

  if (field.type === "recipientsTable") {
    return (activity.recipientEntries || []).some((row) => row.recipientName || row.recipientType || row.contractLink);
  }

  if (field.type === "transferTable") {
    return (activity.transferEntries || []).some((row) => row.overseasRecipientName || row.crossBorderMechanism || row.fileLink);
  }

  return Boolean(activity[field.key]);
}

function getFieldPreviewValue(activity, field) {
  if (!field.derived) {
    if (!isFieldVisible(activity, field)) {
      return "";
    }

    if (field.type === "multiCheckbox") {
      return (activity[field.key] || [])
        .map((value) => getOptionLabel(field.options, value))
        .filter(Boolean)
        .join("; ");
    }

    if (field.type === "recipientsTable") {
      return (activity.recipientEntries || [])
        .filter((row) => row.recipientType || row.recipientName || row.contractLink)
        .map((row, index) => `${index + 1}. ${row.recipientType || "-"} / ${row.recipientName || "-"}`)
        .join("; ");
    }

    if (field.type === "transferTable") {
      return (activity.transferEntries || [])
        .filter((row) => row.overseasRecipientName || row.crossBorderMechanism || row.fileLink)
        .map((row) => `${row.overseasRecipientName || "-"} / ${row.crossBorderMechanism || "-"}`)
        .join("; ");
    }

    return activity[field.key];
  }

  if (field.key === "rightsAvailable") {
    return computeRights(activity).join("; ");
  }

  if (field.key === "categoriesOfRecipients") {
    return (activity.recipientEntries || [])
      .filter((row) => row.recipientType || row.recipientName)
      .map((row) => `${row.recipientType || "-"}: ${row.recipientName || "-"}`)
      .join("; ");
  }

  if (field.key === "processorContractLink") {
    return (activity.recipientEntries || [])
      .map((row) => row.contractLink)
      .filter(Boolean)
      .join("; ");
  }

  if (field.key === "thirdCountryTransfers") {
    return (activity.transferEntries || [])
      .map((row) => row.overseasRecipientName)
      .filter(Boolean)
      .join("; ");
  }

  if (field.key === "transferSafeguards") {
    return (activity.transferEntries || [])
      .filter((row) => row.crossBorderMechanism || row.fileLink)
      .map((row) => `${row.crossBorderMechanism || "-"}${row.fileLink ? ` (${row.fileLink})` : ""}`)
      .join("; ");
  }

  return activity[field.key];
}

function normalizeActivity(role, activity) {
  const base = createEmptyActivity(role);
  const normalized = {
    ...base,
    ...activity,
    id: activity.id || createId("activity"),
    locked: activity.locked ?? false,
    article6LawfulBasis: Array.isArray(activity.article6LawfulBasis) ? activity.article6LawfulBasis : [],
    article9Condition: Array.isArray(activity.article9Condition) ? activity.article9Condition : [],
    article10Condition: Array.isArray(activity.article10Condition) ? activity.article10Condition : [],
    securityMeasures: Array.isArray(activity.securityMeasures) ? activity.securityMeasures : [],
    recipientEntries: Array.isArray(activity.recipientEntries) ? activity.recipientEntries : [],
    transferEntries: Array.isArray(activity.transferEntries) ? activity.transferEntries : [],
    automatedDecisionImpact: activity.automatedDecisionImpact || "No",
  };

  if (!normalized.recipientEntries.length && (activity.categoriesOfRecipients || activity.processorContractLink)) {
    normalized.recipientEntries = [
      {
        recipientType: activity.categoriesOfRecipients || "",
        recipientName: "",
        contractLink: activity.processorContractLink || "",
      },
    ];
  }

  if (!normalized.transferEntries.length && (activity.thirdCountryTransfers || activity.transferSafeguards)) {
    normalized.transferEntries = [
      {
        overseasRecipientName: activity.thirdCountryTransfers || "",
        crossBorderMechanism: activity.transferSafeguards || "",
        fileLink: "",
      },
    ];
  }

  if (!normalized.securityMeasures.length && typeof activity.securityMeasures === "string" && activity.securityMeasures) {
    normalized.securityMeasures = [activity.securityMeasures];
  }

  if (!normalized.article6LawfulBasis.length && typeof activity.article6LawfulBasis === "string" && activity.article6LawfulBasis) {
    normalized.article6LawfulBasis = [activity.article6LawfulBasis];
  }

  if (!normalized.article9Condition.length && typeof activity.article9Condition === "string" && activity.article9Condition) {
    normalized.article9Condition = [activity.article9Condition];
  }

  if (!normalized.article10Condition.length && typeof activity.article10Condition === "string" && activity.article10Condition) {
    normalized.article10Condition = [activity.article10Condition];
  }

  return normalized;
}

function computeRights(activity) {
  const rights = [
    baseRight("Access", "Art.15", "访问权", "第15条"),
    baseRight("Rectification", "Art.16", "更正权", "第16条"),
    baseRight("Erasure (subject to exceptions)", "Art.17", "删除权（受例外限制）", "第17条"),
    baseRight("Restriction", "Art.18", "限制处理权", "第18条"),
  ];

  if (hasSelection(activity.article6LawfulBasis, "6-1-a") || hasSelection(activity.article9Condition, "9-2-a")) {
    rights.push(baseRight("Withdraw consent", "Art.7(3)", "撤回同意", "第7条第3款"));
  }

  if (hasSelection(activity.article6LawfulBasis, "6-1-e") || hasSelection(activity.article6LawfulBasis, "6-1-f")) {
    rights.push(baseRight("Object", "Art.21(1)", "反对权", "第21条第1款"));
  }

  if (activity.automatedDecisionMaking === "Yes" && activity.automatedDecisionImpact === "Yes") {
    rights.push(baseRight("Automated decision safeguards", "Art.22", "自动化决策保障", "第22条"));
  }

  return rights;
}

function getActivityTitle(activity, role, index) {
  if (role === "controller") {
    return activity.businessFunction || activity.purposeOfProcessing || t("untitledController", { index: index + 1 });
  }

  return activity.processingCategories || activity.controllerDetails || t("untitledProcessor", { index: index + 1 });
}

function getRecordTitle(record, index) {
  return record.productName?.trim() || t("untitledRecord", { index: index + 1 });
}

function createId(prefix) {
  return `${prefix}-${Math.random().toString(36).slice(2, 10)}`;
}

function hasSelection(values, target) {
  return Array.isArray(values) && values.includes(target);
}

function getOptionLabel(options, value) {
  const option = options?.find((item) => item.value === value);
  return option ? tx(option.label) : value;
}

function t(key, params = {}) {
  const template = uiText[state.language]?.[key] ?? uiText.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? `{${token}}`));
}

function tx(value) {
  if (typeof value === "string") {
    return value;
  }
  return value?.[state.language] ?? value?.en ?? "";
}

function localizeOption(option) {
  const map = {
    No: "optionNo",
    Yes: "optionYes",
    "Not started": "optionNotStarted",
    "In progress": "optionInProgress",
    Completed: "optionCompleted",
    "N/A": "optionNA",
  };
  return map[option] ? t(map[option]) : option;
}

function formatPreviewValue(value) {
  if (!value) {
    return "-";
  }
  return localizeOption(value);
}

function formatSavedAt(value) {
  if (!value) {
    return t("notSavedYet");
  }

  const parsed = new Date(value);
  if (Number.isNaN(parsed.getTime())) {
    return value;
  }

  const locale = state.language === "zh" ? "zh-CN" : "en-GB";
  return parsed.toLocaleString(locale, {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
  });
}

function setStatus(key, params = {}) {
  runtimeStatusKey = key;
  runtimeStatusParams = params;
  dom.saveStatus.textContent = t(key, params);
}

function baseRight(enLabel, enArticle, zhLabel, zhArticle) {
  return state.language === "zh" ? `${zhLabel}（${zhArticle}）` : `${enLabel} (${enArticle})`;
}

function deepClone(value) {
  return JSON.parse(JSON.stringify(value));
}
