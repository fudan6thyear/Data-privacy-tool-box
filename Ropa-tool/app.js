function createField({ field, value, onInput, disabled = false }) {
  const wrapper = document.createElement("div");
  wrapper.className = `field${field.wide ? " field--wide" : ""}`;

  const inputId = `${field.key}-${Math.random().toString(16).slice(2)}`;
  const label = createFieldLabel(field, inputId);

  let input;
  if (field.type === "textarea") {
    input = document.createElement("textarea");
    input.classList.add("textarea-field");
    if (field.key === "address") {
      input.classList.add("address-textarea");
      input.rows = 3;
    } else {
      input.classList.add("standard-textarea");
      input.rows = field.wide ? 4 : 3;
    }
  } else if (field.type === "select") {
    input = document.createElement("select");
    const placeholderOption = document.createElement("option");
    placeholderOption.value = "";
    placeholderOption.textContent = t("selectPlaceholder");
    input.appendChild(placeholderOption);
    field.options.forEach((option) => {
      const optionEl = document.createElement("option");
      optionEl.value = option;
      optionEl.textContent = localizeOption(option);
      input.appendChild(optionEl);
    });
  } else {
    input = document.createElement("input");
    input.type = field.type || "text";
  }

  input.id = inputId;
  input.name = field.key;
  input.value = value;
  input.placeholder = tx(field.label);
  input.disabled = disabled;
  const handleValueChange = (event) => {
    onInput(event.target.value);
  };
  input.addEventListener("input", handleValueChange);
  input.addEventListener("change", handleValueChange);

  const help = document.createElement("p");
  help.className = "field-help";
  help.textContent = field.required ? t("fieldHelpRequired") : t("fieldHelpOptional");

  wrapper.append(label, input, help);
  return wrapper;
}

function createMultiCheckboxField(activity, field) {
  const wrapper = document.createElement("div");
  wrapper.className = `field${field.wide ? " field--wide" : ""}`;

  const label = createFieldLabel(field);
  const group = document.createElement("div");
  group.className = "multi-checkbox-group";

  field.options.forEach((option) => {
    const optionLabel = document.createElement("label");
    optionLabel.className = "multi-checkbox-option";

    const input = document.createElement("input");
    input.type = "checkbox";
    input.checked = hasSelection(activity[field.key], option.value);
    input.disabled = activity.locked;
    input.addEventListener("change", () => {
      const next = new Set(Array.isArray(activity[field.key]) ? activity[field.key] : []);
      if (input.checked) {
        next.add(option.value);
      } else {
        next.delete(option.value);
      }
      activity[field.key] = Array.from(next);

      if (field.key === "article6LawfulBasis" && !hasSelection(activity[field.key], "6-1-f")) {
        activity.legitimateInterests = "";
        activity.liaLink = "";
      }

      activity.updatedAt = new Date().toISOString();
      markActivityEdited(field.key === "article6LawfulBasis" || field.key === "article9Condition");
    });

    const text = document.createElement("span");
    text.textContent = tx(option.label);

    optionLabel.append(input, text);
    group.appendChild(optionLabel);
  });

  const help = document.createElement("p");
  help.className = "field-help";
  help.textContent = field.required ? t("fieldHelpRequired") : t("fieldHelpOptional");

  wrapper.append(label, group, help);
  return wrapper;
}

